                <h2>Policy and research</h2>
                <p>
                    We believe that influencing policy at different levels is essential to supporting DSI to grow and
                    scale its impact. Therefore, over the course of the project we are engaging with policymakers and
                    key influencers at the European, national and city levels to shape the future of DSI. Furthermore,
                    we are developing an experimental index of how policymakers are supporting DSI across Europe, which
                    aims to highlight best practice and spur collaboration between countries and cities.
                </p>
                <p>
                    Finally, we are carrying out exciting research into emerging technologies like blockchain and
                    artificial intelligence and assessing what they mean for DSI. We are analysing emerging trends and
                    building different future scenarios for DSI, with the aim to help shape a more positive future which
                    harnesses technology for positive social impact.
                </p>
