                <h1 class="content-h1">Introduction</h1>
                <p class="intro">

                </p>
                <p>
                    The past few years have seen a rapid growth in the number of people using digital technologies to
                    tackle social challenges in areas ranging from healthcare and education to democracy and the
                    environment. This phenomenon, which we call <a href="http://digitalsocial.eu/">digital social
                        innovation</a> (DSI), aims to:
                </p>
                <ul>
                    <li>reorient technology to social ends, and to harness it to improve lives and benefit the many
                        rather than the few;
                    <li>empower citizens to take more control over their lives, and to use their collective knowledge
                        and skills to positive effect;
                    <li>make government more accountable and transparent;
                    <li>foster and promote alternatives to the dominant technological and business models — alternatives
                        which are open and collaborative rather than closed and competitive;
                    <li>use technology to create a more environmentally sustainable society.
                </ul>
                <p>
                    Despite the many inspiring initiatives taking place across Europe and the world, relatively few have
                    grown to deliver positive social impact at scale, for a range of reasons which we and others
                    <a href="http://bit.ly/DSINext">have explored in depth</a>.
                </p>
                <h1 class="content-h1" style="padding-top:10px">About the DSI4EU project</h1>
                <p>
                    The latest phase of DSI4EU, running from January 2018 to June 2019, is being carried out by a
                    consortium of seven partner organisations: Nesta (UK), Waag (Netherlands), betterplace lab
                    (Germany), Fab Lab Barcelona (Spain), WeMake (Italy), Barcelona Activa (Spain) and the ePaństwo
                    Foundation (Poland). Find out more about the partners <a href="http://digitalsocial.eu/partners">here</a>.
                </p>
                <p>
                    Over the next year and a half, we will be carrying out a range of activities to support DSI in
                    Europe.
                </p>
                <h2>The digitalsocial.eu platform</h2>
                <p>
                    At the heart of the project is the digitalsocial.eu platform, a lively hub for DSI through which
                    users can:
                </p>
                <ul>
                    <li>showcase their work through <a href="http://digitalsocial.eu/register">project and organisation
                            profiles</a>, and tag their organisations as part of networks like research alliances or
                        membership bodies;
                    <li>explore the DSI community in detail through our searchable database and <a
                                href="http://digitalsocial.eu/viz">data visualisation</a>;
                    <li>identify <a href="http://digitalsocial.eu/funding">funding and support</a> opportunities, as
                        well as DSI-related <a href="http://digitalsocial.eu/events">events</a>, across Europe;
                    <li>find inspiration and stories of DSI through <a href="https://digitalsocial.eu/case-studies">case
                            studies</a>, <a href="http://digitalsocial.eu/blog">blogs</a> and <a
                                href="https://digitalsocial.eu/open-data-research-and-resources">research</a>.
                </ul>
