                <p>
                    Alongside the digitalsocial.eu platform, partner organisations are coordinating six thematic DSI
                    clusters. Within each cluster, we are organising a programme of events, learning and networking
                    opportunities and engaging with policymakers to advocate for committed and effective DSI support.
                </p>
                <p>
                    The clusters are based around the following social areas:
                </p>
